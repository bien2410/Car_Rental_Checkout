package model;

import java.io.Serializable;

/**
 *
 * @author ADMIN
 */
public class Mortgage implements Serializable{
    private String id;
    private String name;

    public Mortgage() {
        super();
    }
    
    public Mortgage(String id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
